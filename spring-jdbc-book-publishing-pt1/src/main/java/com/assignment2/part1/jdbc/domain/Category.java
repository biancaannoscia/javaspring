package com.assignment2.part1.jdbc.domain;

public class Category {
	private long id;
	private String name;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String toString() {
		return "Category - Id: " + id + ", Name: " + name;
	}
	

}
